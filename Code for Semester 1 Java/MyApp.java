public class MyApp{
	public static void main(String[] args){
		//program start
		double x=88;
		double y=8;
		double z=5;
		double ans1=x+y+z;
		double ans2=x*y*z;
		double ans3=x/y/z;
		System.out.println(ans1);
		System.out.println(ans2);
		System.out.println(ans3);
		/*
			* is for multiplication
			/ is for division
			- is for subtraction
					% is for modulus
		*/
	}
}

